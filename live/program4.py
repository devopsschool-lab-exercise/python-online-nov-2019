# # This is a Python Demo - Dict

D = {'name': 'Bob',
     'age': 25,
     'job': 'Dev'}

print(D['name'])    # Bob
print(D['age'])    
print(D['job'])    

# Adding a KEY:VALUE to DICT.

E = {'name': 'Bob',
     'age': 25,
     'job': 'Dev'}

E['city'] = 'New York'
print(E)
# {'name': 'Bob', 'age': 25, 'job': 'Dev', 'city': 'New York'}
