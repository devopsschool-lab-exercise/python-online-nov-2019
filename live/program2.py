# This is a Python Demo - Operators

x = 6
y = 2

# addition
print(x + y)	# 8

# subtraction
print(x - y)	# 4

# multiplication
print(x * y)	# 12

# division
print(x / y)	# 3

# equal to
print(x == y)	# False

# not equal to
print(x != y)	# True

# less than
print(x < y)	# False

# and
print(x > 0 and y < 0)			# True